import { hasLocale } from "next-intl";
import { notFound } from "next/navigation";
import type { Metadata } from "next";
import localFont from "next/font/local";
import { setRequestLocale } from "next-intl/server";
import { routing } from "@/i18n/routing";
import { Toaster } from "@/components/ui/toaster";
import Providers from "./../../components/providers/index";

// Fonts
const geistSans = localFont({
  src: "./fonts/GeistVF.woff",
  variable: "--font-geist-sans",
  weight: "100 900",
});
const geistMono = localFont({
  src: "./fonts/GeistMonoVF.woff",
  variable: "--font-geist-mono",
  weight: "100 900",
});

const zain = localFont({
  src: "./fonts/Zain-Regular.ttf",
  variable: "--font-zain",
  weight: "100 900",
});
const edwardian = localFont({
  src: "./fonts/Edwardian.ttf",
  variable: "--font-edwardian",
  weight: "100 900",
});

//Metadata
export const metadata: Metadata = {
  title: "Create Next App",
  description: "Generated by create next app",
};
// Generate all possible locale-based route parameters for Next.js to pre-render at build time
export function generateStaticParams() {
  return routing.locales.map((locale) => ({ locale }));
}

export default async function LocaleLayout({ children, params }: LayoutProps) {
  const { locale } = params;
  // Enable static rendering
  setRequestLocale(locale);
  if (!hasLocale(routing.locales, locale)) {
    return notFound();
  }

  return (
    <html lang={locale} dir={locale === "ar" ? "rtl" : "ltr"}>
      <body
        className={`${geistSans.variable} ${edwardian.variable} ${geistMono.variable} ${zain.variable} antialiased`}
      >
        {/* Providers */}
        <Providers>
          {/* Main children  */}
          {children}

          {/* Toast notifications */}
          <Toaster />
        </Providers>
      </body>
    </html>
  );
}
